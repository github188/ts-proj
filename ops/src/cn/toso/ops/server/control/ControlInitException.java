package cn.toso.ops.server.control;

public class ControlInitException extends Exception {

	private static final long serialVersionUID = -5281273094829158762L;

	public ControlInitException() {
		super();
	}

	public ControlInitException(String message, Throwable cause) {
		super(message, cause);
	}

	public ControlInitException(String message) {
		super(message);
	}

	public ControlInitException(Throwable cause) {
		super(cause);
	}

}
